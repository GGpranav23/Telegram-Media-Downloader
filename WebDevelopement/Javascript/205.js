// Object Methods 

let Employee = {
    empId: 101,
    location: "Mumbai",
    Salary: 12000
}

// Returns an array of keys of object(Employee)
let result = Object.keys(Employee)
console.log(result);


// Returns an array of values of object(Employee)
let values = Object.values(Employee)
console.log(values);


// Returns an array of Key: Value Pair of Object(Employee)
let entries  = Object.entries(Employee)
console.log(entries);


